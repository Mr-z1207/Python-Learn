print('整数', 888)
print('浮点数', 1.2e-5)
print('字符串', 'I am Leo')
# \ 转义符
print('字符串', 'I\'m Leo')
print('字符串', '123\n456')
# Python还允许用r''表示''内部的字符串默认不转义
print('字符串', r"I'm Leo")
# Python允许用'''...'''的格式表示多行内容
print('字符串', '''
    123
    456
    789''')
# python 大小写敏感，注意布尔值首字母大写
# 布尔值可以用and、or和not运算
print('布尔值', True)
print('布尔值', False)
print('布尔值', 5 > 1)
print('空值', None)
